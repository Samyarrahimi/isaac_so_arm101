##
# MDP settings
##

@configclass
class ActionsCfg:
    """Action specifications for the MDP."""

    #arm_action: ActionTerm = MISSING
    gripper_action: ActionTerm = MISSING


@configclass
class ObservationsCfg:
    """Observation specifications for the MDP."""

    @configclass
    class PolicyCfg(ObsGroup):
        """Observations for policy group."""

        # observation terms (order preserved)
        joint_pos = ObsTerm(func=mdp.joint_pos_rel, noise=Unoise(n_min=-0.01, n_max=0.01))
        joint_vel = ObsTerm(func=mdp.joint_vel_rel, noise=Unoise(n_min=-0.01, n_max=0.01))
        actions = ObsTerm(func=mdp.last_action)

        def __post_init__(self):
            self.enable_corruption = True
            self.concatenate_terms = True

    # observation groups
    policy: PolicyCfg = PolicyCfg()


@configclass
class EventCfg:
    """Configuration for events."""
    reset_all = EventTerm(func=mdp.reset_scene_to_default, mode="reset")

    reset_gripper_position = EventTerm(func=my_mdp.randomize_gripper_position, mode="reset")


@configclass
class RewardsCfg:
    """Reward terms for the MDP."""
    close_gripper = RewTerm(func=my_mdp.reward_gripper_close, weight=10.0, params={"close_value": 0.1})

    close_gripper_p = RewTerm(func=my_mdp.reward_gripper_close, weight=5.0, params={"close_value": 0.3})


    # action penalty
    action_rate = RewTerm(func=mdp.action_rate_l2, weight=-0.0001)

    joint_vel = RewTerm(
        func=mdp.joint_vel_l2,
        weight=-0.0001,
        params={"asset_cfg": SceneEntityCfg("robot")},
    )


@configclass
class TerminationsCfg:
    """Termination terms for the MDP."""

    time_out = DoneTerm(func=mdp.time_out, time_out=True)


@configclass
class CurriculumCfg:
    """Curriculum terms for the MDP."""

    action_rate = CurrTerm(
        func=mdp.modify_reward_weight, params={"term_name": "action_rate", "weight": -0.005, "num_steps": 10000}
    )

    joint_vel = CurrTerm(
        func=mdp.modify_reward_weight, params={"term_name": "joint_vel", "weight": -0.001, "num_steps": 10000}
    )

    close_gripper_p = CurrTerm(
        func=mdp.modify_reward_weight, params={"term_name": "close_gripper_p", "weight": 1, "num_steps": 10000}
    )


